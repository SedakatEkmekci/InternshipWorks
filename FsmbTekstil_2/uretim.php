<h1 align="center">Üretim</h1>

 <img class="img" src="img/uretim.jpg" width="550" height="370">
<p> FSMB Tekstil prestijini, entegrasyonunu tamamlayan teknolojik üretim tesislerinden alır. 214 adet Picanol tezgahı ile boya-terbiye kapasitesinin büyük bir kısmına yeten dokuma tesislerinde dokunmuş ham kumaşlar, Firmamızın son teknolojiye sahip boya-terbiye işletmesinde 100gr/metrekare den 500 gr/metrekare ye kadar değişik tuşelerde konfeksiyona hazır hale getirilmektedir. Son yıllarda yaptığı çevre dostu teknolojik yatırımlarıyla; FSMB Tekstil sayısız elyaf karışımından oluşan 18Ne’den 80Ne’ye iplik üretim çeşitliliğiyle beraber, kumaş üretim kapasitesi yıllık 18 milyon metreyi bulmaktadır.

İplikten kumaşa; üretimin tüm aşamalarının izlenebilir olmasını sağlayan FSMB Tekstil, üretimin her sürecinde fizik ve kalite kontrol laboratuvarında uluslararası standartlara uygun test yapmakta ve müşterilerine servis vermektedir. FSMB Tekstil’in üretim politikası; gerek elyaf boyalı gerekse reaktif ve dispers boyalı ve dijital baskı yapma kapasitesine sahip tesislerinde müşterilerin taleplerine en kısa sürede, en iyi kalitede ve uygun fiyatla, çevre duyarlığından ödün vermeden hizmet vermektir.  <br><br><br><br> <p>



<h2 align="center">DOKUMA</h2>

 <img class="img" src="img/dokuma.jpg" width="550" height="320">

Bursa Demirtaş Organize Sanayi Bölgesinde bulunan ve yıllık 12 milyon metre kumaş üretim kapasitesi olan firmamız, tamamı klimalı ve kirli hava temizleme sistemine sahiptir.

Toplam 35.000 metrekare kapalı alan üzerinde, 144 armür + 16 jakar toplam 160 adet en son teknolojili dokuma, 2 adet Karl Mayer, 2 adet Benninger marka Çözgü hazırlama ve 20 adet kalite kontrol makinası, 310 deneyimli çalışanı, dinamik yapısı, prensipli ve dürüst çalışma anlayışıyla faaliyetlerini devam ettirmektedir.

Firmamız; AR-GE ve ürün geliştirme departmanlarında istihdam ettiği konusunda uzman tekstil ve tasarım mühendisleri ile kumaş, desen ve moda tasarımı anlayışında yeni standartlar ortaya koymakta ve ilkbahar-yaz, sonbahar-kış olmak üzere yılda iki farklı ana koleksiyon hazırlamaktadır. Bu koleksiyonları her yıl düzenli olarak katıldığı Texworld-Paris, Textillegprom Moscov, The London Textile Fair fuarlarında müşterileri ile buluşturmaktadır. <br><br><br><br><br>



<h2 align="center">BOYAHANE</h2>

 <img class="img" src="img/boyahane.jpg" width="550" height="280">

Boyahanemiz, müşterilerimizin istemiş olduğu kriterleri baz alarak kaliteli, hızlı ve diyalog çerçevesi içinde çalışmaktadır.

Boyahanemiz, dokuma ve örme kumaş parkuruna uygun olup, likralı ve likrasız her türlü elyaf ve elyaf karışımını boyayıp geniş aksesuar ve finish işlem makinalarımızla mamul kumaşları müşterilerimize servis etmektedir. Günlük kapasitemiz günün moda kumaşlarına göre değişmekte olup, 15-20 ton aralığındadır. Kullandığımız kimyasal madde ve boyar maddeler uluslararası standartlara uygun olup “OEKO TEX” belgelidir.

Emprime ayağında dijital baskı makinalarımız son sistem olup, FSMB Tekstil tesislerinde imal edilen fantazi kumaşlara uzman desen, tasarım ve coloris ekibiyle iş ortaklarına kalite ile farklılığı sunmaktadır. Aylık kapasitemiz 400.000 ile 450.000 mt. arasındadır.

Müşteri memnuniyeti temel ilkemizdir.

